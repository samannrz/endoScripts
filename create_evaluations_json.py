def merge_evaluations(file_names):
    new_dict = {'evals': []}
    for f in file_names:
        json_eval = open(f)
        eval = json.load(json_eval)
        evals = eval['evals']
        for e in evals:
            new_dict['evals'].append(e)

    json_object = json.dumps(new_dict, indent=4)
    with open('Evaluation_all' + '.json', 'w') as o:
        o.write(json_object)

